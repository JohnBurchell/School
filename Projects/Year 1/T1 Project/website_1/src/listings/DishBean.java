package listings;

import java.io.Serializable;

/**
 * @author <b>John</b>
 * <br><br>Simple dish object that holds information about dishes.<br>
 */

public class DishBean implements Serializable{
	
	
private static final long serialVersionUID = 1L;
private String dishName, restName, dishType, dishDescription, dishCuisine, dishContains, dishOwner,dishId, dishPrice;

	public DishBean () {
		
	}
	

	public String getDishName() {		
		
		return this.dishName;

	}

	public void setDishName(String dishName) {

		this.dishName = dishName;

	}
	
	public String getDishId() {

		return this.dishId;
		
	}
	
	public void setDishId(String dishId) {
		
		this.dishId = dishId;
	}
	
	public String getRestName() {

		return this.restName;
		
	}
	
	public void setRestName(String restName) {	

		this.restName = restName;	
		
	}
	
	public String getDishPrice() {

		return this.dishPrice;

	}

	public void setDishPrice(String dishPrice) {

		this.dishPrice = dishPrice;

	}
	
	public String getDishType() {

		return this.dishType;
		
	}
	
	public void setDishType(String dishType) {

		this.dishType = dishType;
		
	}
	
	public String getDishDesc() {

		return this.dishDescription;
		
	}
	
	public void setDishDesc(String dishDescription) {

		this.dishDescription = dishDescription;
		
	}
	
	public String getDishContains() {

		return this.dishContains;
		
	}
	
	public void setDishContains(String dishContains) {

		this.dishContains = dishContains;
		
	}
	
	public String getDishCuisine() {

		return this.dishCuisine;
		
	}
	
	public void setDishCuisine(String dishCuisine) {

		this.dishCuisine = dishCuisine;
		
	}

	public String getDishOwner() {

		return this.dishOwner;
		
	}
	
	public void setDishOwner(String dishOwner) {

		this.dishOwner = dishOwner;
		
	}


}


