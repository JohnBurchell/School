package listings;


import java.io.Serializable;

/**
 * @author <b>John</b>
 * <br><br>Simple bean that provides a structure for the<br> 
 * user details, the object is used in many classes.<br>
 */

public class AdminBean implements Serializable{
	
	
private static final long serialVersionUID = 1L;
private String ownerFirstName, ownerLastName, ownerUserName, ownerPassword, ownerAddress, ownerScore, ownerPhoneNumber, ownerAccountLevel;

	
	public AdminBean () {
		
	}	

	public String getOwnerFirstName() {		
		
		return ownerFirstName;

	}

	public void setOwnerFirstName(String ownerFirstName) {

		this.ownerFirstName = ownerFirstName;

	}
	
	
	public String getOwnerLastName() {

		return ownerLastName;
		
	}
	
	public void setOwnerLastName(String ownerLastName) {	

		this.ownerLastName = ownerLastName;	
		
	}

	
	public String getOwnerUserName() {

		return ownerUserName;
		
	}
	
	public void setOwnerUserName(String ownerUserName) {

		this.ownerUserName = ownerUserName;
		
	}
	
	public String getOwnerPassword() {

		return ownerPassword;
		
	}
	
	public void setOwnerPassword(String ownerPassword) {

		this.ownerPassword = ownerPassword;
		
	}
	
	public String getOwnerScore() {

		return ownerScore;
		
	}
	
	public void setOwnerScore(String ownerScore) {

		this.ownerScore = ownerScore;
		
	}
	
	public String getOwnerPhoneNumber() {

		return ownerPhoneNumber;
		
	}
	
	public void setOwnerPhoneNumber(String ownerPhoneNumber) {

		this.ownerPhoneNumber = ownerPhoneNumber;
		
	}			
	
	public void setOwnerAccountLevel(String ownerAccountLevel) {
		
		this.ownerAccountLevel = ownerAccountLevel;		
	}
	
	public String getOwnerAccountLevel() {
		
		return ownerAccountLevel;
	}

	public String getOwnerAddress() {
		return ownerAddress;
	}

	public void setOwnerAddress(String ownerAddress) {
		this.ownerAddress = ownerAddress;
	}


}


