package listings;

import java.io.Serializable;

/**
 * @author <b>John</b>
 * <br><br>Simple bean object that holds information about restaurants.<br>
 */

public class RestBean implements Serializable{
	
	
private static final long serialVersionUID = 1L;
private String restName, restAddress, restOpenHours, restDescrip, restScore, restVotes, restType, ownerUserName, restNumber;

	
	public RestBean () {
		
	}	

	public String getRestName() {		
		
		return this.restName;

	}

	public void setRestName(String restName) {

		this.restName = restName;

	}
	
	
	public String getRestAddress() {

		return this.restAddress;
		
	}
	
	public void setRestAddress(String restAddress) {	

		this.restAddress = restAddress;	
		
	}

	
	public String getRestOpenHours() {

		return this.restOpenHours;
		
	}
	
	public void setOpenHours(String restOpenHours) {

		this.restOpenHours = restOpenHours;
		
	}
	
	public String getRestDescrip() {

		return this.restDescrip;
		
	}
	
	public void setRestDescrip(String restDescrip) {

		this.restDescrip = restDescrip;
		
	}
	
	public String getRestScore() {

		return this.restScore;
		
	}
	
	public void setRestScore(String restScore) {

		this.restScore = restScore;
		
	}
	
	public String getRestVotes() {

		return this.restVotes;
		
	}
	
	public void setRestVotes(String restVotes) {

		this.restVotes = restVotes;
		
	}

	public String getRestType() {

		return this.restType;		
	}
	
	public void setRestType(String restType) {

		this.restType = restType;
		
	}
	
	public String getOwnerUserName () {
		
		return ownerUserName;
	}
	
	public void setOwnerUserName(String ownerUserName) {
		
		this.ownerUserName = ownerUserName;
	}
	
	public String getRestNumber() {
		
		return restNumber;		
	}
	
	public void setRestNumber(String restNumber) {
		
		this.restNumber = restNumber;
	}


}


